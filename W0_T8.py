# Importing pi and tau from the math module
from math import pi, tau

# Comparing pi and tau
if pi == tau:
    print("Tau equals Pi.")
else:
    print("Tau doesn't equal Pi.")

# Comparing pi multiplied by 2 with tau
if 2 * pi == tau:
    print("Tau equals 2 * Pi.")
else:
    print("Tau doesn't equal 2 * Pi.")
